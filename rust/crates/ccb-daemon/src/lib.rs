use thiserror::Error;

#[derive(Error, Debug)]
pub enum DaemonError {
    #[error("io error: {0}")]
    Io(#[from] std::io::Error),
    #[error("json error: {0}")]
    Json(#[from] serde_json::Error),
    #[error("storage error: {0}")]
    Storage(#[from] ccb_storage::StorageError),
    #[error("config error: {0}")]
    Config(String),
}

pub type Result<T> = std::result::Result<T, DaemonError>;
